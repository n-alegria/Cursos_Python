# Modulo matematicas
import math

print("Raiz cuadrada de 10: ", math.sqrt(10))

print("Numero pi: ", float(math.pi))

# Redondear hacia arriba
print("Redondear arriba: ", math.ceil(6.56789))

# Redondear hacia abajo
print("Redondear abajo: ", math.floor(6.56789))

# Modulo random
import random

# .rendint(a, b) -> numero random entre ese rango
print("Numero aleatorio entre 15 y 67: ", random.randint(15, 67))
