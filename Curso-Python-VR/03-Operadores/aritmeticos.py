# Operadores aritmeticos -> son operadores matematicos

# Operador de asignacion -> = (asigno un numero a una variable)
numeroUno = 77
numeroDos = 44

# Operadores: -, +, *, /, %
resta = numeroUno - numeroDos
suma = numeroUno + numeroDos
multiplicacion = numeroUno * numeroDos
division = numeroUno / numeroDos
resto = numeroUno % numeroDos

print("********* CALCULADORA *********")
print(f"La resta es: {resta}")
print(suma)
print("La multiplicacion es: {}".format(multiplicacion))
print("La division es: " + str(division))
print("El resto de la division es", resto) # con la ',' puedo concatenar numeros